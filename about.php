        <p>
        Regarding the research experience. At present, due to the shortage of organ donors, the increasing number of patients on the transplant waiting lists, and an ever-increasing aging population, solely relying on donated tissues and organs is not practical. Most of medical applications of 3D printing are still static, non-living constructs designed to serve as structural or space-filling scaffolds. To address the problem, my previous researches are mainly related to investigating the effect of the physical and chemical cues of extracellular environment (ECM) on cellular properties, fabricating ECMs which are physiologically related with the real conditions in vivo by means of 3D printing and possibly realizing the real bio-fabricated transplantable tissues and organs in the future.</a>.</p>
